package esercizio1;

public class Cane {

	// Attributi
	public String razza;
	public String nome;
	public int eta;
	
	// Costruttore vuoto
	public Cane() {}
	
	// Costruttore parametrizzato
	public Cane(String razza, String nome, int eta) {
		this.razza = razza;
		this.nome = nome;
		this.eta = eta;
		}
}
