package esercizio1;

public class Animale {
	
	// Attributi
	public String regno;
	public String classe;
	public String specie;
	
	// Costruttore vuoto
	public Animale() {}
	
	// Costruttore parametrizzato
	public Animale(String regno, String classe, String specie) {
		this.regno = regno;
		this.classe = classe;
		this.specie = specie;
	}
	
}
