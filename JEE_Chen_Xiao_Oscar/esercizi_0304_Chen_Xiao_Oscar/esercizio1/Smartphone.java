package esercizio1;

public class Smartphone {

	// Attributi
	public String marca;
	public String modello;
	public float prezzo;
	
	// Costruttore vuoto
	public Smartphone() {}
	
	// Costruttore parametrizzato
	public Smartphone(String marca, String modello, float prezzo) {
		this.marca = marca;
		this.modello = modello;
		this.prezzo = prezzo;
		}
}
